package cap02;
/** Exemplo comentarios;
 * Essa classe demonstra a utilização de variáveis em uma classe
 * em Java. São declaradas variáveis do tipo int e double.
 * O exemplo também  demonstra como imprimir o conteudo das variaveis na tela
 */
public class Comentarios {
    public static void main(String[] args) {
        int matricula = 1089, idade = 20; //declaração de variáveis do tipo inteiro
        double dolar = 2.62;
        /* As linhas seguintes enviam o conteudo das
        variveis para a tela
        */
        System.out.println("Matricula: " + matricula + " idade: " + idade + " dolar: " + dolar);
    }
}
